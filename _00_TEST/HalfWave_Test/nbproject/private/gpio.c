/**********************************************************************************************
 *名    称：void GPIO_Init(void)
 *
 *参    数：无
 *
 *返 回 值：无
 *
 *描    述：GPIO初始化
 *********************************************************************************************/
void GPIO_Init(void)
{
/*-------------------------------------IO口输出配置------------------------------------------*/
    /*GPIOA输出配置--------------------------------------------------------------------------*/ 
    
    /*GPIOB输出配置--------------------------------------------------------------------------*/ 
    
    /*GPIOC输出配置--------------------------------------------------------------------------*/ 
    //485输入输出控制端------------------------PC2
    TRISCbits.TRISC2 = 0;
    /*GPIOD输出配置--------------------------------------------------------------------------*/ 
    //继电器控制端TON9-------------------------PD0
    //继电器控制端TON10------------------------PD1
    //PLC输出DB4-------------------------------PD2
    //PLC输出DB3-------------------------------PD3
    //PLC输出DB2-------------------------------PD4
    //PLC输出DB1-------------------------------PD5
    //可控硅控制端QD2--------------------------PD6
    //可控硅控制端QD1--------------------------PD7
    TRISDbits.TRISD0 = 0;
    TRISDbits.TRISD1 = 0;
    TRISDbits.TRISD2 = 0;
    TRISDbits.TRISD3 = 0;
    TRISDbits.TRISD4 = 0;
    TRISDbits.TRISD5 = 0;
    TRISDbits.TRISD6 = 0;
    TRISDbits.TRISD7 = 0;
    /*GPIOE输出配置--------------------------------------------------------------------------*/ 
    //继电器控制端TON1-------------------------PE0
    //继电器控制端TON2-------------------------PE1
    //继电器控制端TON3-------------------------PE2
    //继电器控制端TON4-------------------------PE3
    //继电器控制端TON5-------------------------PE4
    //继电器控制端TON6-------------------------PE5
    //继电器控制端TON7-------------------------PE6
    //继电器控制端TON8-------------------------PE7
    TRISEbits.TRISE0 = 0;
    TRISEbits.TRISE1 = 0;
    TRISEbits.TRISE2 = 0;
    TRISEbits.TRISE3 = 0;
    TRISEbits.TRISE4 = 0;
    TRISEbits.TRISE5 = 0;
    TRISEbits.TRISE6 = 0;
    TRISEbits.TRISE7 = 0;
    /*GPIOF输出配置--------------------------------------------------------------------------*/
    
    /*GPIOG输出配置--------------------------------------------------------------------------*/
    //LED--------------------------------------PG0
    TRISGbits.TRISG0 = 0;
    
/*-------------------------------------IO口输入配置------------------------------------------*/    
    /*GPIOA输入配置--------------------------------------------------------------------------*/ 
    //PLC输入RC_3------------------------------PA4
    //PLC输入RC_4------------------------------PA5
    TRISAbits.TRISA4 = 1;
    TRISAbits.TRISA5 = 1;
    ANCON0bits.ANSEL4 = 0;
    
    /*GPIOB输入配置--------------------------------------------------------------------------*/ 
    
    /*GPIOC输入配置--------------------------------------------------------------------------*/ 
    //PLC输入RC_1------------------------------PC0
    //PLC输入RC_2------------------------------PC1
    //过零检测输入-----------------------------PC3
    TRISCbits.TRISC0 = 1;
    TRISCbits.TRISC1 = 1;
    TRISCbits.TRISC3 = 1;
    
    /*GPIOD输入配置--------------------------------------------------------------------------*/
    
    /*GPIOE输入配置--------------------------------------------------------------------------*/
    
    /*GPIOF输入配置--------------------------------------------------------------------------*/
    
    /*GPIOG输入配置--------------------------------------------------------------------------*/

/*-------------------------------------IO输出初始化------------------------------------------*/    
    PORTA = 0;
    PORTB = 0;
    PORTC = 0;
    PORTD = 0;
    PORTE = 0;
    PORTF = 0;
    PORTG = 0;
}

