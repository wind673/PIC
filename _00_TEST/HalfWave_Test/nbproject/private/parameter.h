#ifndef __PARAMETER_H
#define __PARAMETER_H

/* 包含 --------------------------------------------------------------------------------------*/
#include "flash.h"
#include "stdint.h"
/* 私有宏 ------------------------------------------------------------------------------------*/
#define PARAM_NUM                          16     //系统参数个数                  
#define _GEARS_MAGNE                       1      //充磁档位
#define _GEARS_DEMAG                       2        //退磁档位
//通道使能channel 1~8
#define _CHANNEL_SW                        3      //通道开关(16位变量，每一位对应一个通道，相应位置"1"表示开通道，置"0"表示关通道)                 	   
#define _ELEC_SETOVER                      4      //电流偏置
#define _ELEC_COEFF                        5      //电流传感器系数
#define _PULSE                             6      //脉冲个数
#define _LOAD_INVERT                       7      //负载反相（1:同相 0:反相）
#define _ELEC_MIN                          8      //电流最小值  (充退磁小于此值发出警报)
#define _ELEC_MAX                          9      //电流最大值  (充退磁大于此值发出警报)
#define _MAGNET_ELEC_THRE                  10     //充磁饱和阀值
#define _DEMAG_ELEC_THRE                   11     //退磁饱和阀值
#define _DEMAG_MODE                        12     //退磁方式（1:脉冲退磁 0:振荡退磁）
#define _OPERATE_SPACING                   13     //连续操作时间间隔
#define _PULSE_THRE                        14     //充退磁饱和脉冲个数
#define _PULSE_SPACING                     15     //充退磁脉冲间隔
#define _PLC_KEY_MODE                      16     //PLC按键模式选择（1:长按键 0:短按键）
/* 私有结构体 --------------------------------------------------------------------------------*/
typedef struct                             
{                                          
	uint16_t Value;    //参数值              
	uint16_t Min;  	   //参数最小值          
	uint16_t Max;      //参数最大值          
	uint16_t Default;  //参数默认值          
}__Parameter;                              
/* 函数声明 ----------------------------------------------------------------------------------*/
void Parameter_Init(void);

#endif

