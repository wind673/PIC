/**********************************************************************************************
 *名    称：void delay_us(uint32_t nus)
 *
 *参    数：uint32_t nus
 *
 *返 回 值：无
 *
 *描    述：延时函数 单位：us
 *********************************************************************************************/
void delay_us(uint32_t nus)
{
    while(nus--);
}
/**********************************************************************************************
 *名    称：void delay_ms(uint32_t nms)
 *
 *参    数：uint32_t nms
 *
 *返 回 值：无
 *
 *描    述：延时函数 单位：ms
 *********************************************************************************************/
void delay_ms(uint32_t nms)
{
    nms = nms*1000;
    while(nms--);
}

