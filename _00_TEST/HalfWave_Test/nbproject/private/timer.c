/**********************************************************************************************
 *名    称：void Timer_Init(void)
 *
 *参    数： 无
 *
 *返 回 值：无
 *
 *描    述： 系统定时器初始化
 *********************************************************************************************/
void Timer_Init(void)
{
    OpenTimer0( TIMER_INT_ON &      //使能timer0中断
                T0_16BIT &          //定义为16位定时器
                T0_SOURCE_INT &     //选择内部时钟
                T0_PS_1_8 );        //时钟不分频
    INTCON |= 0x80;                 //使能全局中断
    WriteTimer0(65536-50000);        //写入初始值
    
    /*可在本段添加timer1-timer8的初始化函数...*/
    
    
}


